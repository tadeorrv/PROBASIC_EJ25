#-Unidad Central de procesamiento (CPU)
#Equivalente humano: El cerebro.  
#La CPU se encarga de procesar y ejecutar instrucciones. Es como el cerebro humano que recibe información, la analiza y toma decisiones.  

#-Memoria Principal (RAM) 
#Equivalente humano* La memoria a corto plazo.  
#La memoria principal almacena datos temporales y de trabajo que la CPU necesita de inmediato, pero se pierde al apagar el sistema. Es como la memoria a corto plazo del ser humano.

#-Memoria Secundaria 
#Equivalente humano: La memoria a largo plazo.  
#La memoria secundaria (como discos duros) almacena datos de manera permanente hasta que se borren. Es como la memoria a largo plazo del ser humano.

#-Dispositivos de Entrada 
#Equivalente humano: Los sentidos.  
#Los dispositivos de entrada proporcionan información al sistema, como los sentidos lo hacen al cuerpo humano.  

#Dispositivos de Salida 
#Equivalente humano: El habla o los movimientos.  
#Los dispositivos de salida muestran o proyectan resultados del sistema, como cuando un humano comunica información o actúa físicamente.  

